function on() {
    document.getElementById("overlay").style.display = "block";
    document.getElementById("_nav_bar_logo").style.display = "none";
    document.getElementById("_nav_bar_hamb").style.display = "none";
  }
  
  function off() {
      document.getElementById("overlay").style.display = "none";
      document.getElementById("_nav_bar_logo").style.display = "block";
      document.getElementById("_nav_bar_hamb").style.display = "block";
  }